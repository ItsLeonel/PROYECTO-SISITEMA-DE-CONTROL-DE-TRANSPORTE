package Logica.Entidades;

public class ReglaSancion {

    private String codigoTipoIncidencia;
    private int posicionesRetroceso;

    public String getCodigoTipoIncidencia() {
        return codigoTipoIncidencia;
    }

    public void setCodigoTipoIncidencia(String codigoTipoIncidencia) {
        this.codigoTipoIncidencia = codigoTipoIncidencia;
    }

    public int getPosicionesRetroceso() {
        return posicionesRetroceso;
    }

    public void setPosicionesRetroceso(int posicionesRetroceso) {
        this.posicionesRetroceso = posicionesRetroceso;
    }
}
